//Addition Table:
 
#include<stdio.h>
void main()
{
 int i,table;
 printf("Addition Table of 10: \n");
 for(i=0;i<=10;i++)
 {
  table=i+10;
  printf("%d + 10 = %d\n",i,table);
 }
}
